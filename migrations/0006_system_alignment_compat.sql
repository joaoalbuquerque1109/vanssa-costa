create or replace function log_booking_notification()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'INSERT' then
    insert into booking_notifications (agendamento_id, event_type, payload)
    values (
      new.id,
      'booking_created',
      jsonb_build_object(
        'status', new.status,
        'funcionario', new.funcionario,
        'cliente', new.cliente,
        'data', new.data,
        'hora', new.hora
      )
    );
    return new;
  end if;

  if tg_op = 'UPDATE' and coalesce(old.status, '') <> coalesce(new.status, '') then
    insert into booking_notifications (agendamento_id, event_type, payload)
    values (
      new.id,
      'booking_status_changed',
      jsonb_build_object('old_status', old.status, 'new_status', new.status)
    );
    return new;
  end if;

  return new;
end;
$$;

