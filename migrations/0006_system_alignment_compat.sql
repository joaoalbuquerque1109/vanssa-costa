create or replace function resync_employee_services_from_legacy(legacy_employee_id bigint)
returns void
language plpgsql
as $$
declare
  v_employee_id bigint;
begin
  select id into v_employee_id
  from employees
  where legacy_user_id = legacy_employee_id
  limit 1;

  if v_employee_id is null then
    return;
  end if;

  delete from employee_services
  where employee_id = v_employee_id;

  insert into employee_services (employee_id, service_id)
  select v_employee_id, s.id
  from servicos_func sf
  join services s on s.legacy_service_id = sf.servico
  where sf.funcionario = legacy_employee_id
  on conflict do nothing;
end;
$$;

