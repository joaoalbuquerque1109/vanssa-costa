-- Global cross-table uniqueness for CPF and email (clientes <-> usuarios)
create or replace function enforce_global_identity_uniqueness()
returns trigger
language plpgsql
as $$
declare
  new_cpf text;
  new_email text;
  conflict_id bigint;
begin
  new_cpf := regexp_replace(coalesce(new.cpf, ''), '\D', '', 'g');
  new_email := lower(coalesce(new.email, ''));

  if length(new_cpf) <> 11 then
    raise exception 'CPF invalido para %: %', tg_table_name, new.cpf;
  end if;

  if new_email = '' then
    raise exception 'Email obrigatorio para %', tg_table_name;
  end if;

  if tg_table_name = 'usuarios' then
    select c.id into conflict_id
    from clientes c
    where regexp_replace(c.cpf, '\D', '', 'g') = new_cpf
       or lower(c.email) = new_email
    limit 1;

    if conflict_id is not null then
      raise exception 'CPF/Email ja cadastrado em clientes (id=%)', conflict_id;
    end if;
  elsif tg_table_name = 'clientes' then
    select u.id into conflict_id
    from usuarios u
    where regexp_replace(u.cpf, '\D', '', 'g') = new_cpf
       or lower(u.email) = new_email
    limit 1;

    if conflict_id is not null then
      raise exception 'CPF/Email ja cadastrado em usuarios (id=%)', conflict_id;
    end if;
  end if;

  return new;
end;
$$;

drop trigger if exists trg_usuarios_global_identity on usuarios;
create trigger trg_usuarios_global_identity
before insert or update of cpf, email on usuarios
for each row
execute function enforce_global_identity_uniqueness();

drop trigger if exists trg_clientes_global_identity on clientes;
create trigger trg_clientes_global_identity
before insert or update of cpf, email on clientes
for each row
execute function enforce_global_identity_uniqueness();

