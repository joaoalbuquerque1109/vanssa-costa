    break_start = excluded.break_start,
    break_end = excluded.break_end,
    updated_at = now();
end;
$$;

create or replace function sync_employee_work_hours_from_legacy()
returns trigger
language plpgsql
as $$
begin
  if tg_op = 'DELETE' then
    perform resync_employee_work_hours_from_legacy(old.funcionario);
    return old;
  end if;

  perform resync_employee_work_hours_from_legacy(new.funcionario);
  return new;
end;
$$;

